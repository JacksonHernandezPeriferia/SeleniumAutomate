package TiposDeDatos;

public class TiposDeDatos {
	
	public static void main(String[]args) {
        
		int i = 4523; 
		long  l = 652345;
		double d1 = 56.2354;
		double d2 = 12456;
		char c = 'd';
		boolean t = true;
		String str = "Cómo es";
		
		System.out.println("Integer Var Is --> "+i);
		System.out.println("Long Var -->"+l);	
		System.out.println("double Var d1 -->"+d1);
		System.out.println("double Var d2 -->"+d2);
		System.out.println("char Var Is -->"+c);
		System.out.println("boolean Var Is -->"+t);
		System.out.println("boolean Var str Is -->"+str);
	}

}
